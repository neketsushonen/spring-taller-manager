package cl.lai.manager.taller.vo;

import java.util.List;

public class Vendedor  extends VO{
	
	private String nombre, direccion,rut,nick,telefono,password;
	
	public String getTelefono() {
		return telefono;
	}
	public void setTelefono(String telefono) {
		this.telefono = telefono;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getDireccion() {
		return direccion;
	}
	public void setDireccion(String direccion) {
		this.direccion = direccion;
	}
	public String getRut() {
		return rut;
	}
	public void setRut(String rut) {
		this.rut = rut;
	}
	public String getNick() {
		return nick;
	}
	public void setNick(String nick) {
		this.nick = nick;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	@Override
	public String toString() {
		return "Vendedor [nombre=" + nombre + ", direccion=" + direccion + ", rut=" + rut + ", nick=" + nick
				+ ", telefono=" + telefono + ", password=" + password + "]";
	}
	
}
